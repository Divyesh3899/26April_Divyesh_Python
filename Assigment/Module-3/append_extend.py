# append :- it is use to adds single element in the end of the list.
# extend :- it is use to add a list in the end of the another list.

# append example 

a=['Divyesh','Nirav','chandni','bhavik','Nidhi','Yasvi','Gopi']
print(a)
print(a)
a.append('patel')
print(a)

# extend example

a=['Divyesh','Nirav','chandni','bhavik','Nidhi','Yasvi','Gopi']
print(a)
b=['Patel','kotadia','paghdar']
a.extend(b)
print(a)